window.setLang = function (lang) {
  const dict = window.I18N[lang] || window.I18N.ru;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] != null) el.innerHTML = dict[key];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (dict[key] != null) el.placeholder = dict[key];
  });
  document.querySelectorAll('[data-lang]').forEach(b => b.classList.toggle('is-active', b.dataset.lang === lang));
  window.__uf_lang = lang;
  try { localStorage.setItem('usc_lang', lang); } catch (e) {}
  if (window.__uf_onLangChange) window.__uf_onLangChange(lang);
};
